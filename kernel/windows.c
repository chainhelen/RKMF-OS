static void set_palette(int start, int end, unsigned char *rgb)
{
	int i;
	out_byte(0x03c8, start);
	for (i = start; i <= end; i++) {
		out_byte(0x03c9, rgb[0] / 4);
		out_byte(0x03c9, rgb[1] / 4);
		out_byte(0x03c9, rgb[2] / 4);
		rgb += 3;
	}
}



